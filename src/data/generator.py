_DISTRIBUTION = {
    "inquiry": 60,
    "complaint": 50,
    "clearance_objection": 40,
    "meeting_request": 25,
    "review_request": 25,
}


def generate_tickets(count: int = 200) -> list[TicketCreate]:
    tickets: list[TicketCreate] = []
    index = 1

    # Scale distribution to requested count
    total = sum(_DISTRIBUTION.values())
    for request_type, base_count in _DISTRIBUTION.items():
        type_count = round(base_count / total * count)
        templates = _TEMPLATES[request_type]

        for _ in range(type_count):
            est_type = random.choice(_ESTABLISHMENT_TYPES)
            product_type = random.choice(_PRODUCT_TYPES)
            template = random.choice(templates)
            filled = _fill_template(template, est_type, product_type)

            name = filled["submitter_name"]
            email_user = name.replace(" ", ".").lower()
            email = f"{email_user}@{random.choice(_EMAILS_DOMAINS)}"

            ticket = TicketCreate(
                domain_id="sfda",
                source_system="seed",
                source_ticket_id=f"SEED-{index:04d}",
                subject=filled["subject"],
                description=filled["description"],
                submitter_name=name,
                submitter_email=email,
                custom_fields={
                    "establishment_type": est_type,
                    "product_type": product_type,
                    "request_type": request_type,
                    "establishment_license": filled["license"],
                },
            )
            tickets.append(ticket)
            index += 1

    random.shuffle(tickets)
    return tickets[:count]



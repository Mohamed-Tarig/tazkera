def _license_number(est_type: str) -> str:
    prefix = {"warehouse": "WH", "factory": "FAC", "retail": "RTL", "importer": "IMP"}
    return f"{prefix[est_type]}-2024-{random.randint(1000, 9999)}"


def _ticket_number(index: int) -> str:
    date_str = datetime.now().strftime("%Y%m%d")
    return f"TZK-{date_str}-{index:04d}"


def _random_date(days_back: int = 90) -> datetime:
    delta = timedelta(days=random.randint(0, days_back), hours=random.randint(0, 23))
    return datetime.now(timezone.utc) - delta


